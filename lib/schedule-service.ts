/**
 * Get TOTP session for a lecture session
 * @param lectureSessionId - Lecture session ID
 */
export async function getTOTPSession(
  lectureSessionId: string
): Promise<{ session: TOTPSession | null; error: Error | null }> {
  try {
    const { data, error } = await supabase
      .from('totp_sessions')
      .select('*')
      .eq('lecture_session_id', lectureSessionId)
      .order('created_at', { ascending: false })
      .limit(1)
      .single();

    if (error && error.code !== 'PGRST116') {
      throw error;
    }

    return { session: data as TOTPSession | null, error: null };
  } catch (error) {
    console.error('Error fetching TOTP session:', error);
    return { session: null, error: error as Error };
  }
}

/**
 * Subscribe to real-time updates for a lecture session's TOTP code
 * @param lectureSessionId - Lecture session ID
 * @param onUpdate - Callback when TOTP code updates
 */
export function subscribeTOTPUpdates(
  lectureSessionId: string,
  onUpdate: (session: TOTPSession) => void
) {
  const channel = supabase
    .channel(`totp-session-${lectureSessionId}`)
    .on(
      'postgres_changes',
      {
        event: '*',
        schema: 'public',
        table: 'totp_sessions',
        filter: `lecture_session_id=eq.${lectureSessionId}`,
      },
      (payload) => {
        onUpdate(payload.new as TOTPSession);
      }
    )
    .subscribe();

  return channel;
}

/**
 * Helper: Format time from HH:mm:ss to HH:mm AM/PM
 */
export function formatTime(timeString: string | null): string {
  if (!timeString) return '';

  const [hours, minutes] = timeString.split(':');
  const hour = parseInt(hours, 10);
  const minute = minutes;
  const period = hour >= 12 ? 'PM' : 'AM';
  const formattedHour = hour % 12 || 12;

  return `${formattedHour}:${minute} ${period}`;
}

/**
 * Helper: Calculate duration in minutes from start and end times
 */
export function calculateDuration(startTime: string, endTime: string): number {
  const [startHour, startMin] = startTime.split(':').map(Number);
  const [endHour, endMin] = endTime.split(':').map(Number);

  const startMinutes = startHour * 60 + startMin;
  const endMinutes = endHour * 60 + endMin;

  return endMinutes - startMinutes;
}

