/**
 * Cache for schedule data
 */
interface CacheEntry {
  data: LectureSessionExtended[];
  timestamp: number;
}

const scheduleCache: Map<string, CacheEntry> = new Map();
const CACHE_DURATION = 5 * 60 * 1000; // 5 minutes

/**
 * Semester date cache
 */
interface SemesterDateEntry {
  startDate: string;
  endDate: string;
  timestamp: number;
}

const semesterDateCache: Map<string, SemesterDateEntry> = new Map();
const SEMESTER_CACHE_DURATION = 24 * 60 * 60 * 1000; // 24 hours

/**
 * Fetch semester start and end dates
 * @param semesterId - Semester ID
 * @returns Object with startDate and endDate (YYYY-MM-DD format) or null
 */
export async function getSemesterDates(
  semesterId: string
): Promise<{ startDate: string; endDate: string } | null> {
  try {
    // Check cache first
    const cached = semesterDateCache.get(semesterId);
    if (cached && Date.now() - cached.timestamp < SEMESTER_CACHE_DURATION) {
      console.log('[Schedule Service] ✓ Semester dates from cache');
      return { startDate: cached.startDate, endDate: cached.endDate };
    }

    console.log('[Schedule Service] 📚 Fetching semester dates for:', semesterId.substring(0, 8) + '...');
    
    const { data, error } = await supabase
      .from('semesters')
      .select('start_date, end_date')
      .eq('id', semesterId)
      .single();

    if (error) {
      console.error('[Schedule Service] Error fetching semester dates:', error);
      return null;
    }

    const result = {
      startDate: data.start_date,
      endDate: data.end_date,
    };

    // Cache the result
    semesterDateCache.set(semesterId, {
      ...result,
      timestamp: Date.now(),
    });

    console.log('[Schedule Service] ✓ Semester date range:', result.startDate, 'to', result.endDate);
    return result;
  } catch (error) {
    console.error('[Schedule Service] Exception in getSemesterDates:', error);
    return null;
  }
}

