/**
 * Calculate display status for a session based on current time
 * Returns 'completed', 'ongoing', or 'upcoming'
 * @param session - Lecture session with date and time info
 * @returns Display status
 */
export function calculateSessionDisplayStatus(
  session: LectureSession
): 'completed' | 'ongoing' | 'upcoming' {
  const now = new Date();
  const today = `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`; // local IST date, not UTC
  const currentTimeStr = now.toTimeString().substring(0, 8); // HH:mm:ss — already local time

  // Session is in the past → completed
  if (session.session_date < today) {
    return 'completed';
  }

  // Session is in the future → upcoming
  if (session.session_date > today) {
    return 'upcoming';
  }

  // Session is today → check time
  // Get the end time (prefer actual_end_time if it exists)
  const endTime = session.actual_end_time || session.end_time || session.scheduled_end_time;
  const startTime = session.scheduled_start_time || session.start_time;

  if (!endTime || !startTime) {
    // If times are not available, assume upcoming
    return 'upcoming';
  }

  // If current time is past end time → completed
  if (currentTimeStr > endTime) {
    return 'completed';
  }

  // If current time is before start time → upcoming
  if (currentTimeStr < startTime) {
    return 'upcoming';
  }

  // Otherwise → ongoing
  return 'ongoing';
}

/**
 * Fetch lecture sessions for a student
 * Filters by: university_id → student enrollments → section_id → lecture sessions
 * @param studentId - Student user ID
 * @param universityId - University ID
 * @param startDate - Start date (YYYY-MM-DD)
 * @param endDate - End date (YYYY-MM-DD)
 */
export async function getStudentSchedule(
  studentId: string,
  universityId: string,
  startDate: string,
  endDate: string
): Promise<{ sessions: LectureSessionExtended[] | null; error: Error | null }> {
  try {
    // Check cache first
    const cacheKey = `${studentId}-${startDate}-${endDate}`;
    const cached = scheduleCache.get(cacheKey);
    if (cached && Date.now() - cached.timestamp < CACHE_DURATION) {
      console.log('[Schedule Service] ✓ Schedule data from cache');
      return { sessions: cached.data, error: null };
    }

    console.log(
      `[Schedule Service] ========================================`
    );
    console.log(
      `[Schedule Service] 🔍 QUERY PARAMETERS:`
    );
    console.log(
      `[Schedule Service]   - studentId: ${studentId}`
    );
    console.log(
      `[Schedule Service]   - universityId: ${universityId}`
    );
    console.log(
      `[Schedule Service]   - dateRange: ${startDate} to ${endDate}`
    );
    console.log(
      `[Schedule Service] ========================================`
    );

    // Step 1: Get student enrollments with section details
    console.log(
      `[Schedule Service] STEP 1: Querying student_enrollments...`
    );
    const { data: enrollments, error: enrollmentError } = await supabase
      .from('student_enrollments')
      .select('section_id, batch')
      .eq('user_id', studentId)
      .eq('university_id', universityId)
      .eq('is_active', true);

    if (enrollmentError) throw enrollmentError;

    console.log(
      `[Schedule Service]   ✓ Query result: Found ${enrollments?.length || 0} enrollments`
    );
    if (enrollments && enrollments.length > 0) {
      console.log(
        `[Schedule Service]   Sample enrollments (first 2): ${JSON.stringify(
          enrollments.slice(0, 2),
          null,
          2
        )}`
      );
    } else {
      console.warn(
        `[Schedule Service]   ⚠️  NO ENROLLMENTS FOUND - Student may not be enrolled in any sections`
      );
    }

    if (!enrollments || enrollments.length === 0) {
      console.warn('[Schedule Service] No active enrollments found for student');
      return { sessions: [] as LectureSessionExtended[], error: null };
    }

    const sectionIds = enrollments.map((e) => e.section_id);

    // Step 2: Get lecture sessions for those sections
    console.log(
      `[Schedule Service] STEP 2: Querying lecture_sessions for ${sectionIds.length} section(s)...`
    );
    console.log(
      `[Schedule Service]   sectionIds: [${sectionIds.map((id) => id.substring(0, 8)).join(', ')}...]`
    );
    const { data: sessions, error: sessionsError } = await supabase
      .from('lecture_sessions')
      .select(
        `
        id,
        university_id,
        timetable_id,
        course_id,
        section_id,
        room_id,
        instructor_ids,
        session_date,
        scheduled_start_time,
        scheduled_end_time,
        start_time,
        end_time,
        actual_start_time,
        actual_end_time,
        totp_required,
        session_status,
        is_special_class,
        max_late_minutes,
        attendance_open,
        attendance_close_time,
        is_active,
        is_cancelled,
        created_at,
        updated_at,
        courses(id, code, name),
        rooms(id, room_number, room_name, building:buildings(id, name))
      `
      )
      .in('section_id', sectionIds)
      .eq('university_id', universityId)
      .eq('is_active', true)
      .eq('is_cancelled', false)
      .gte('session_date', startDate)
      .lte('session_date', endDate)
      .order('session_date', { ascending: true })
      .order('start_time', { ascending: true });

    if (sessionsError) throw sessionsError;

    console.log(
      `[Schedule Service]   ✓ Query result: Found ${sessions?.length || 0} lecture sessions`
    );
    if (sessions && sessions.length > 0) {
      console.log(
        `[Schedule Service]   Sample sessions (first 2): ${JSON.stringify(
          sessions.slice(0, 2).map((s: any) => ({
            id: s.id,
            course: s.courses?.name,
            code: s.courses?.code,
            date: s.session_date,
            time: `${s.start_time} - ${s.end_time}`,
            room: s.rooms?.room_name || s.rooms?.room_number,
            building: s.rooms?.building?.name,
            status: s.session_status,
            instructorIds: s.instructor_ids?.length || 0,
          })),
          null,
          2
        )}`
      );
    } else {
      console.warn(
        `[Schedule Service]   ⚠️  NO SESSIONS FOUND - Check if sections have lecture_sessions in the database for the date range`
      );
    }

    // Step 3: Fetch instructor names for instructor_ids array
    console.log('[Schedule Service] STEP 3: Enriching sessions with instructor details...');
    const extendedSessions = await Promise.all(
      (sessions || []).map(async (session: any, idx: number) => {
        let instructors: Array<{ id: string; name: string }> | undefined;

        if (session.instructor_ids && session.instructor_ids.length > 0) {
          if (idx === 0) {
            console.log(
              `[Schedule Service]   Fetching instructors for sessions...`
            );
          }
          const { data: instructorData, error: instructorError } = await supabase
            .from('instructors')
            .select('id, name')
            .in('id', session.instructor_ids);

          if (instructorError) {
            console.warn(
              `[Schedule Service] Error fetching instructors for session ${session.id}:`,
              instructorError
            );
          }

          instructors = instructorData || [];
        }

        const extendedSession = {
          ...session,
          instructors,
          course: session.courses,
          room: session.rooms,
          displayStatus: calculateSessionDisplayStatus(session),
        };

        return extendedSession;
      })
    );

    console.log(`[Schedule Service] ✓ Retrieved ${extendedSessions.length} sessions with displayStatus`);

    // Cache the result
    scheduleCache.set(cacheKey, {
      data: extendedSessions as LectureSessionExtended[],
      timestamp: Date.now(),
    });

    return { sessions: extendedSessions as LectureSessionExtended[], error: null };
  } catch (error) {
    console.error('[Schedule Service] ❌ Error fetching student schedule:', error);
    console.log(
      `[Schedule Service] ========================================`
    );
    return { sessions: null, error: error as Error };
  }
}

